#include "Globals.hpp"

Game* getGame(Game* instance)
{
	static Game* inst;

	if(instance == nullptr)
	{
		return inst;
	}
	else
	{
		inst = instance;
		return nullptr;
	}
}

int main()
{
	{
		for(int i = 0; i < 5; ++i)
		{
			sptr<Game> game(new Game());
			getGame(game.get());
			game->initialize();
			getGame()->run();
		}
	}

	_CrtDumpMemoryLeaks();
	return 0;
}
